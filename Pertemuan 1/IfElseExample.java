public class IfElseExample {
	public static void main(String[] args) {
		int number=13;

		if(number%2==0){
			System.out.println("Angka genap");
		}else{
			System.out.println("Angka ganjil");
		}
	}
}
/*penjelasan program diatas
	program diatas digunakan untuk mencoba If-Else statement, dimana if akan dilakukan jika nilainya true,
	apabila nilainya false maka akan dilakukan else. 
	Pada code diatas outputnya angka ganjil karena statement if bernilai false, dimana 13 tidak sama dengan 0. 
	Maka pernyataan didalam else yang keluar sebagai output.
*/